'''
Crie um programa para ler duas entradas de Strings fornecidas pelo usuário. 
Verifique se as strings são iguais ou diferentes. 
Imprima uma mensagem na saída padrão indicando o resultado da verificação.
'''

frase1 = input('Digite a primeira sentença para comparação: ')
frase2 = input('Digite a segunda sentença para comparação: ')

if frase1 == frase2:
    print('As sentenças digitadas são idênticas!')
else:
    print('As sentenças digitadas não são idênticas!')